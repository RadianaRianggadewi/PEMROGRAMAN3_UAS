package com.example.catalogscluster.models;

public class Rumah {
	private String nama;
	private String stok;
	private String desc;
	
	public String getNama() {
		return nama;
	}
	
	public void setNama(String nama) {
		this.nama = nama;
	}
	
	public String getStok() {
		return stok;
	}
	
	public void setStok(String stok) {
		this.stok = stok;
	}
	
	public String getDesc() {
		return desc;
	}
	
	public void setDesc(String desc) {
		this.desc = desc;
	}
}